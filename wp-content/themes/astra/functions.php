/**
 * Function to upgrade user role based on points during checkout.
 */
function upgrade_user_role_based_on_points_during_checkout($order_id) {
    if (!function_exists('wc_points_rewards_get_users_points')) {
        return;
    }

    $order = wc_get_order($order_id);
    $user_id = $order->get_user_id();
    $user = new WP_User($user_id);

    $total_points = wc_points_rewards_get_users_points($user_id);

    $gold_threshold = 50000; // points equivalent to $50,000
    $diamond_threshold = 100000; // points equivalent to $100,000

    if ($total_points >= $diamond_threshold) {
        $user->set_role('diamond_customer');
    } elseif ($total_points >= $gold_threshold) {
        $user->set_role('gold_customer');
    }
}
add_action('woocommerce_checkout_order_processed', 'upgrade_user_role_based_on_points_during_checkout');




















/**
 * Custom function to apply a discount automatically for Gold and Diamond customers.
 */
function apply_discount_for_special_customers($cart) {
    if (is_admin() && !defined('DOING_AJAX')) {
        return;
    }

    $user_id = get_current_user_id();
    $user = new WP_User($user_id);

    if (in_array('diamond_customer', $user->roles)) {
        $discount = $cart->subtotal * 0.15; // 15% discount for Diamond customers
        $cart->add_fee(__('Diamond Customer Discount', 'woocommerce'), -$discount);
    } elseif (in_array('gold_customer', $user->roles)) {
        $discount = $cart->subtotal * 0.10; // 10% discount for Gold customers
        $cart->add_fee(__('Gold Customer Discount', 'woocommerce'), -$discount);
    }
}
add_action('woocommerce_cart_calculate_fees', 'apply_discount_for_special_customers');
