/**
 * Function to upgrade user role based on total spending history.
 */
function upgrade_user_role_based_on_total_spending_history($user_id) {
    $user = new WP_User($user_id);

    $gold_threshold = 50000; // Amount equivalent to $50,000
    $diamond_threshold = 100000; // Amount equivalent to $100,000

    // Get all orders of the user
    $args = array(
        'customer_id' => $user_id,
        'status' => array('completed', 'processing') // Include only completed and processing orders
    );
    $orders = wc_get_orders($args);

    $total_spent = 0;

    // Calculate total spending
    foreach ($orders as $order) {
        $total_spent += $order->get_total();
    }

    // Check if the user meets the spending thresholds
    if ($total_spent >= $diamond_threshold) {
        $user->set_role('diamond_customer');
    } elseif ($total_spent >= $gold_threshold) {
        $user->set_role('gold_customer');
    }
}

/**
 * Function to periodically check and upgrade user role.
 */
function check_and_upgrade_user_role() {
    $user_id = get_current_user_id();
    if ($user_id) {
        upgrade_user_role_based_on_total_spending_history($user_id);
    }
}
add_action('wp_ajax_check_and_upgrade_user_role', 'check_and_upgrade_user_role');
add_action('wp_ajax_nopriv_check_and_upgrade_user_role', 'check_and_upgrade_user_role');

/**
 * Enqueue the JavaScript file for periodic role checking.
 */
function enqueue_user_role_update_script() {
    wp_enqueue_script('user-role-update', get_template_directory_uri() . '/js/user-role-update.js', array('jquery'), null, true);
    wp_localize_script('user-role-update', 'userRoleUpdate', array(
        'ajax_url' => admin_url('admin-ajax.php')
    ));
}
add_action('wp_enqueue_scripts', 'enqueue_user_role_update_script');

